const solveKojun = require("../kojun");

test("10x10", () => {
  let valuesInputGrid = [
    [5, 0, 2, 0, 2, 0, 3, 1, 3, 1],
    [0, 4, 0, 1, 0, 5, 0, 5, 0, 4],
    [7, 5, 1, 7, 0, 0, 3, 1, 3, 0],
    [0, 4, 0, 0, 0, 0, 0, 0, 0, 3],
    [2, 0, 3, 4, 0, 2, 0, 0, 4, 0],
    [5, 0, 2, 0, 6, 0, 0, 0, 0, 0],
    [0, 1, 3, 0, 1, 0, 0, 4, 0, 3],
    [6, 7, 0, 3, 0, 1, 4, 0, 0, 1],
    [4, 0, 3, 0, 4, 0, 0, 0, 0, 3],
    [0, 1, 0, 2, 0, 6, 2, 0, 2, 1]
  ];
  const regionsInputGrid = [
    [1, 5, 5, 5, 9, 9, 9, 9, 20, 20],
    [1, 1, 1, 5, 6, 6, 13, 13, 20, 13],
    [2, 2, 1, 6, 6, 12, 14, 13, 13, 13],
    [2, 2, 6, 6, 10, 12, 14, 14, 14, 18],
    [2, 2, 2, 6, 10, 10, 15, 18, 18, 18],
    [3, 3, 7, 7, 7, 10, 16, 16, 21, 21],
    [3, 3, 3, 7, 7, 11, 17, 19, 21, 21],
    [4, 4, 3, 7, 11, 11, 17, 19, 22, 22],
    [4, 4, 8, 8, 8, 8, 17, 19, 19, 23],
    [4, 4, 4, 8, 8, 8, 17, 17, 23, 23]
  ];
  const solution = [
    [5, 3, 2, 4, 2, 4, 3, 1, 3, 1],
    [2, 4, 3, 1, 3, 5, 6, 5, 2, 4],
    [7, 5, 1, 7, 1, 2, 3, 1, 3, 2],
    [6, 4, 2, 6, 4, 1, 2, 4, 1, 3],
    [2, 1, 3, 4, 3, 2, 1, 2, 4, 1],
    [5, 6, 2, 5, 6, 1, 2, 1, 2, 4],
    [4, 1, 3, 4, 1, 3, 5, 4, 1, 3],
    [6, 7, 2, 3, 2, 1, 4, 3, 2, 1],
    [4, 2, 3, 5, 4, 7, 3, 2, 1, 3],
    [3, 1, 5, 2, 1, 6, 2, 1, 2, 1]
  ];

  solveKojun(valuesInputGrid, regionsInputGrid);
  expect(valuesInputGrid).toStrictEqual(solution);
});
